/* demonstrates the use of char data type (numerical type)
 * the assignment is done via numeric value 
 * and the out is using literal char
 */

#include<stdio.h>

int main(){
	char c;  // declare char type variable
	c=100;   // store a value in the variable
	printf("Value is: %c\n",c); // display the value of the variable
	return 0;
}
